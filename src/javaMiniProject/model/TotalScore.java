package javaMiniProject.model;

public class TotalScore {
	private double allScoreSum;
	private int semesterCPSum;
	private double semesterScoreSum;
	private double semesterScoreAvg;
	
	public double getAllScoreSum() {
		return allScoreSum;
	}
	public void setAllScoreSum(double allScoreSum) {
		this.allScoreSum = allScoreSum;
	}
	public int getSemesterCPSum() {
		return semesterCPSum;
	}
	public void setSemesterCPSum(int semesterCPSum) {
		this.semesterCPSum = semesterCPSum;
	}
	public double getSemesterScoreSum() {
		return semesterScoreSum;
	}
	public void setSemesterScoreSum(double semesterScoreSum) {
		this.semesterScoreSum = semesterScoreSum;
	}
	public double getSemesterScoreAvg() {
		return semesterScoreAvg;
	}
	public void setSemesterScoreAvg(double semesterScoreAvg) {
		this.semesterScoreAvg = semesterScoreAvg;
	}
	
	

}
